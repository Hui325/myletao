$(function () {
   $('#form').bootstrapValidator({
     fields:{
       username:{
         validators:{
           //不能为空
           notEmpty:{
             message:"用户名不能为空"
           },
           //校验长度
           stringLength:{
             min:2,
             max:6,
             message:"用户名长度必须在2-6位"
           }
         }
       },
       password:{
         validators:{
           //不能为空
           notEmpty:{
             message:"密码不能为空"
           },
           //校验长度
           stringLength:{
             min:6,
             max:12,
             message:"密码长度必须在6-12位"
           }
         }
       }
     }
   });
});
